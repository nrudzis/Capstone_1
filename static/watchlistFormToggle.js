$('#watchlist-form-section').hide();

$('#watchlist-form-section-show').click(() => {
  $('#watchlist-form-section-show').hide();
  $('#watchlist-form-section').show();
});

$('#watchlist-form-section-hide').click(() => {
  $('#watchlist-form-section').hide();
  $('#watchlist-form-section-show').show();
});
